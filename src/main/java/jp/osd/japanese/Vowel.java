package jp.osd.japanese;

/**
 * 母音を表す列挙型です。
 * 
 * @author asuka
 * @since 0.2.0
 */
public enum Vowel {
    /** 母音が「a」、すなわち 50 音表のあ段にある仮名であることを表します。 */
    A, 
    /** 母音が「i」、すなわち 50 音表のい段にある仮名であることを表します。 */
    I, 
    /** 母音が「u」、すなわち 50 音表のう段にある仮名であることを表します。 */
    U, 
    /** 母音が「e」、すなわち 50 音表のえ段にある仮名であることを表します。 */
    E, 
    /** 母音が「o」、すなわち 50 音表のお段にある仮名であることを表します。 */
    O
}
